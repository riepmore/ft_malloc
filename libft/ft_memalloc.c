#include "libft.h"
#include <stdlib.h>

void		*ft_memalloc(size_t size)
{
	void	*mem;

	if ((mem = malloc(sizeof(void *) * size)) == NULL)
		return (NULL);
	ft_bzero(mem, size);
	return (mem);
}
