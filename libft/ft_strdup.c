#include "libft.h"
#include <stdlib.h>

char		*ft_strdup(const char *s1)
{
	char	*str;
	int		i;

	if ((str = (char *)malloc(sizeof(char) * ft_strlen(s1) + 1)) == NULL)
		return (NULL);
	i = -1;
	while (s1[++i])
		str[i] = s1[i];
	str[i] = '\0';
	return (str);
}
