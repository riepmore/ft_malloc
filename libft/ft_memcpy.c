#include "libft.h"

void		*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*srctemp;
	char	*dsttemp;

	srctemp = (char *)src;
	dsttemp = (char *)dst;
	while (n--)
		*dsttemp++ = *srctemp++;
	return (dst);
}
